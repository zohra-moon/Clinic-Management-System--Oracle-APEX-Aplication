create or replace PROCEDURE  "PRC_PRESCRIBE_MEDICINES" ( 
    P_CONSULTATION_ID IN NUMBER,  -- Consultation for which medicines are prescribed 
    P_DISEASE_id IN number  -- Disease name to fetch medicines 
  --  P_DOC_ID IN NUMBER            -- Doctor prescribing the medicine 
) 
AS 
    -- Cursor to get medicines related to the disease 
    CURSOR CUR_MEDICINES IS 
    SELECT M.MED_ID, M.MED_NAME , 
DP.FREQUENCY, DP.INTAKE_DAYS 
FROM DISEASE_MEDICINE DP  
       JOIN DISEASE D   
       ON (DP.DIS_id = D.DIS_ID) 
        JOIN MEDICINE_info M  ON (M.MED_ID = DP.MED_ID) 
        where dp.dis_id=P_DISEASE_id; 
    -- Variables to hold medicine details 
    V_MED_ID NUMBER; 
    V_MED_NAME VARCHAR2(100); 
    V_INTAKE_DAYS NUMBER; 
    V_FREQUENCY VARCHAR2(50); 
    V_SL_NO NUMBER; 
 
BEGIN 
    -- Loop through all medicines for the selected disease 
    FOR REC IN CUR_MEDICINES LOOP 
        -- Get the next sequence number for the prescription entry 
        SELECT NVL(MAX(SL_NO), 0) + 1 INTO V_SL_NO FROM consult_medicine_details WHERE CONSULTATION_ID = P_CONSULTATION_ID; 
 
        -- Insert into the prescribed medicine table 
        INSERT INTO consult_medicine_details (SL_NO, CONSULTATION_ID) 
        VALUES (V_SL_NO, P_CONSULTATION_ID); 
 
        -- Insert into the medicine details table with dosage information 
        INSERT INTO consult_medicine_details (MED_DETAIL_ID, MED_ID, CONSULTATION_ID, INTAKE_DAYS, FREQUENCY) 
        VALUES (SEQ_MED_DETAIL.NEXTVAL, REC.MED_ID, P_CONSULTATION_ID, REC.INTAKE_DAYS, REC.FREQUENCY); 
    END LOOP; 
 
    -- Commit the transaction 
    COMMIT; 
 
    --DBMS_OUTPUT.PUT_LINE('Medicines prescribed successfully for disease: ' || P_DISEASE_id); 
 
EXCEPTION 
    WHEN OTHERS THEN 
        ROLLBACK; 
        DBMS_OUTPUT.PUT_LINE('Error: ' || SQLERRM); 
END PRC_PRESCRIBE_MEDICINES;
