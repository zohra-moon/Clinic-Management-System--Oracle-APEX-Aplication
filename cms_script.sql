    -- Break down the number 
    crore := FLOOR(a_number / 10000000); 
    lakh := FLOOR(MOD(a_number, 10000000) / 100000); 
    thou := FLOOR(MOD(a_number, 100000) / 1000); 
    hund := FLOOR(MOD(a_number, 1000) / 100); 
    doubl := FLOOR(MOD(a_number, 100) / 10) * 10; 
    sing := MOD(a_number, 10); 
    deci := ROUND((a_number - TRUNC(a_number)) * 100); 
 
    -- Initialize the text variable 
    text := ''; 
 
    -- Add crore part 
    IF crore <> 0 THEN 
        text := text || number_chart(crore) || ' Crore '; 
    END IF; 
 
    -- Add lakh part 
    IF lakh <> 0 THEN 
        IF lakh <= 20 OR lakh MOD 10 = 0 THEN 
            text := text || number_chart(lakh) || ' Lakh '; 
        ELSE 
            text := text || number_chart(lakh - lakh MOD 10) || ' ' || number_chart(lakh MOD 10) || ' Lakh '; 
        END IF; 
    END IF; 
 
    -- Add thousand part 
    IF thou <> 0 THEN 
        IF thou <= 20 OR thou MOD 10 = 0 THEN 
            text := text || number_chart(thou) || ' Thousand '; 
        ELSE 
            text := text || number_chart(thou - thou MOD 10) || ' ' || number_chart(thou MOD 10) || ' Thousand '; 
        END IF; 
    END IF; 
 
    -- Add hundred part 
    IF hund <> 0 THEN 
        IF hund <= 20 OR hund MOD 10 = 0 THEN 
            text := text || number_chart(hund) || ' Hundred '; 
        ELSE 
            text := text || number_chart(hund - hund MOD 10) || ' ' || number_chart(hund MOD 10) || ' Hundred '; 
        END IF; 
    END IF; 
 
    -- Add tens and ones part 
    IF doubl <> 0 THEN 
        IF doubl <= 20 OR doubl MOD 10 = 0 THEN 
            text := text || number_chart(doubl) || ' '; 
        ELSE 
            text := text || number_chart(doubl) || ' ' || number_chart(sing) || ' '; 
        END IF; 
    END IF; 
 
    -- Add paisa part (decimal handling) 
    IF deci > 0 THEN 
        IF deci <= 20 OR deci MOD 10 = 0 THEN 
            text := text || 'and ' || number_chart(deci) || ' Paisa '; 
        ELSE 
            text := text || 'and ' || number_chart(deci - deci MOD 10) || ' ' || number_chart(deci MOD 10) || ' Paisa '; 
        END IF; 
    END IF; 
 
    -- Return the text with "Only" 
    IF text IS NOT NULL THEN 
        RETURN text || 'Only'; 
    ELSE 
        RETURN 'Zero Only'; 
    END IF; 
END SPELL_OUT;
