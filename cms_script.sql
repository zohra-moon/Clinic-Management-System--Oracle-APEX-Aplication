create or replace FUNCTION get_full_age(p_dob DATE) 
    RETURN VARCHAR2 
IS
    v_years NUMBER;
    v_months NUMBER;
    v_days NUMBER;
    v_result VARCHAR2(100);
BEGIN
    -- Calculate the number of years, months, and days
    SELECT FLOOR(MONTHS_BETWEEN(SYSDATE, p_dob) / 12),  -- Years
           FLOOR(MOD(MONTHS_BETWEEN(SYSDATE, p_dob), 12)),  -- Months
           TRUNC(SYSDATE) - ADD_MONTHS(p_dob, FLOOR(MONTHS_BETWEEN(SYSDATE, p_dob) / 12) * 12)  -- Days
    INTO v_years, v_months, v_days
    FROM dual;
    
    -- Combine the result into a single string
    v_result := v_years || ' years, ' || v_months || ' months, ' || v_days || ' days';
    
    RETURN v_result;
END get_full_age;
