create or replace PROCEDURE google_user_map
IS
    l_email   VARCHAR2(255);
    l_user    VARCHAR2(100);
BEGIN
    -- Email returned by Google
    l_email := lower(apex_util.get_session_state('APEX$AUTHENTICATION_EMAIL'));

    SELECT USER_NAME
    INTO   l_user
    FROM   guser
    WHERE  lower(email) = l_email;

    -- Switch to mapped APEX user
    apex_authentication.login(
        p_username => l_user
    );

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        raise_application_error(
            -20001,
            'Your Google account is not linked to any application user.'
        );
END;
